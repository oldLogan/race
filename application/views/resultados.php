					</br>
					</br>
					
					<ul><strong>Alguns documentos são necessários neste processo:</strong>
						<li>•	Documento de identificação (original e cópia)</li>
						<li>•	Carteira nacional de habilitação – CNH (original e cópia)</li>
						<li>•	Cadastro de pessoa física – CPF (original e cópia)</li>
						<li>•	Comprovante de residência (original e cópia)</li>
					</ul>
					</br>
					
					<ul><strong>O passo a passo para adição de categoria acontece da seguinte forma:</strong>
						<li>1.	Deve solicitar o serviço ao Detran ou em uma empresa credenciada;</li>
						<li>2.	Fazer à Avaliação Psicológica complementar, na qual deverá ser considerado apto; se optou pelo exercício de atividade remunerada ao veículo ou se este estiver
						vencido;</li>
						<li>3.	Fazer o Exame de Aptidão Física e Mental, em clínica credenciada, para a categoria que pretende incluir, no qual deverá ser considerado apto;</li>
						<li>4.	Realizar o Curso de Atualização para renovação da CNH ou submeter-se a uma prova, se for o caso;</li>
						<li>5.	Realizar a Atualização no Curso Especializado no qual esteja habilitado;</li>
						<li>6.	Solicitar a emissão da Licença de Aprendizado de Direção Veicular (LADV) ao Detran;</li>
						<li>7.	Realizar o Curso de Prática de Direção Veicular com duração de 15 horas, em veículo compatível com a categoria que pretende adicionar, em Centro de Formação de
						Condutores (CFC), a conhecida autoescola, portando obrigatoriamente a LADV;</li>
						<li>8.	Fazer o Exame preliminar de Prática de Direção Veicular, em veículo da categoria que pretende adicionar, e obter aprovação.</li>
					</ul>
					
