					<h5 align="center"><strong>Resultados das Corridas</strong></h5>
					</br>
					
					<p>A adição de categoria é um processo que acontece quando um condutor já habilitado decide adicionar outra categoria na sua habilitação. Essa adição pode ocorrer para
					quem possui a categoria A e deseja adicionar a categoria B. Sendo possível também, para condutores da categoria B, adicionar a categoria A.</p> 
					<p>Para isso, é necessário que o condutor esteja com a habilitação em situação regular, não pode estar suspensa, cassada, ou até mesmo ter cometido nenhuma infração, 
					de natureza média  nos últimos doze meses. </P>
					<p>Caso inicie o processo de adição quando ainda está sob porte da carteira provisória deve-se concluir o processo de adição dentro da validade da carteira provisória. 
					Ou esperar, e só iniciar o processo de adição da categoria quando ocorrer a substituição da carteira provisória pela definitiva. </P>
					</br>
					
